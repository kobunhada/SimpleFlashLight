/**
 * On/Off play interface.
 *
 * @author kobunhada
 */
public interface PlayOnOff {
    /**
     * Implementation class going to need create a "On" state process.
     */
    void On();

    /**
     * Implementation class going to need create a "Off" state process.
     */
    void Off();

}
